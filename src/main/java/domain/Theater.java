package domain;


import jakarta.persistence.Entity;
import jakarta.persistence.Id;
@Entity // JPA 엔티티로 설정


public class Theater {
    @Id
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}